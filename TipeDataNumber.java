public class TipeDataNumber {
    public static void main(String[] args) {
        // bilangan bulat
        byte TipeDataByte = 127;
        short TipeDataShort = 32767;
        int TipeDataInt = 10;
        long TipeDataLong = 10L;

        // bilangan pecahan
        float TipeDataFloat = 4.4F;
        double TipeDataDouble = 5.5;

        // sistem bilangan
        int decimalInt = 20;
        int hexDecimal = 0xFF;
        int binDecimal = 0b0101;

        int SebuahBilangan = 1_000_000;

        System.out.println(SebuahBilangan);
    }
}
